# Є список рядків:
# words = ["apple", "banana", "apple", "orange", "banana"]
# Використай хеші, щоб визначити кількість унікальних елементів
# без використання set().

# Місце для твого коду:
words=["apple", "banana", "apple", "orange", "banana"]
unique_hashes={}   # <- тут твій код
for w in words:
    h=hash(w)
    if h not in unique_hashes:
        unique_hashes[h]=w


