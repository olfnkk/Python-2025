def selection_sort(arr):

    for i in range(len(arr)):
        m = i
        for j in range(i+1, len(arr)):
            if isinstance(arr[j], str) and isinstance(arr[m], str):
                if arr[j].lower() < arr[m].lower():
                    m = j
            else:
                if arr[j] < arr[m]:
                    m = j
        arr[i], arr[m] = arr[m], arr[i]
    return arr

