# Напиши просту хеш-функцію для рядка:
# 1) переведи всі символи в коди ASCII (ord())
# 2) підсумуй коди
# 3) візьми залишок від ділення на 10
# Перевір на кількох словах і подумай про можливі колізії.

# Місце для твого коду:
def simple_hash(s):
    total=sum(ord(ch) for ch in s)
    return total%10


print(simple_hash("apple"))
print(simple_hash("banana"))
print(simple_hash("orange"))