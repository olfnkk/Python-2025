# ĞœÑ–ÑÑ†Ğµ Ğ´Ğ»Ñ ĞºĞ¾Ğ´Ñƒ â†“â†“â†“â†“â†“â†“â†“â†“â†“â†“â†“â†“â†“â†“â†“â†“â†“â†“â†“â†“â†“â†“â†“â†“â†“â†“â†“â†“â†“â†“â†“â†“â†“â†“â†“â†“â†“â†“â†“â†“â†“â†“â†“â†“â†“â†“

def counting_sort_with_negatives(arr):
    min_val=min(arr)
    max_val=max(arr)

    shift=-min_val
    count=[0]*(max_val - min_val + 1)

    for num in arr:
        count[num + shift]+=1

    result=[]
    for i in range(len(count)):
        result.extend([i - shift] * count[i])

    return result

print(counting_sort_with_negatives([-5, -2, -2, 0, 3, 3, 1]))



